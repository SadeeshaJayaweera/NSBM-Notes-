#include <stdio.h>
#include <stdlib.h>

int main()
{
    /*
    Write a program which uses the format commands with modifiers to print the following output:

    2   4   8
    3   9   27
    4   16  64

    */

    printf("2\t4\t8\n");
    printf("3\t9\t27\n");
    printf("4\t16\t64");
    return 0;
}
